#include"statusdisplay.h"

//==============================================================================================
// コンストラクタ
//==============================================================================================
CStatus::CStatus() : CObject2D(4)
{
}

//==============================================================================================
// デストラクタ
//==============================================================================================
CStatus::~CStatus()
{
}

//==============================================================================================
// 初期化処理
//==============================================================================================
HRESULT CStatus::Init(D3DXVECTOR3 pos)
{
	CObject2D::Init(pos);

	CObject2D::SetTexture(CTexture::TEXTURE_STATUS);	//テクスチャの設定

	return S_OK;
}

//==============================================================================================
// 終了処理
//==============================================================================================
void CStatus::Uninit()
{
	CObject2D::Uninit();
}

//==============================================================================================
// 更新処理
//==============================================================================================
void CStatus::Update()
{
	CObject2D::Update();
}

//==============================================================================================
// 描画処理
//==============================================================================================
void CStatus::Draw()
{
	CObject2D::Draw();
}

//==============================================================================================
// 生成処理
//==============================================================================================
CStatus *CStatus::Create(D3DXVECTOR3 pos, float width, float height)
{
	CStatus *pStatus = nullptr;

	pStatus = new CStatus;

	if (pStatus != nullptr)
	{
		pStatus->Init(pos);
		pStatus->SetSize(width, height);
		pStatus->SetCol(D3DXCOLOR(0.0f, 1.0f, 1.0f, 1.0f));
	}

	return pStatus;
}